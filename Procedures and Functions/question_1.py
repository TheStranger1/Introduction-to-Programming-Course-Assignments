#Write a procedure that gets two parameters, n and m, and proceeds to output a multiplication table with
#values ( 1.n, 2.n...m.n)
#Example output (with argument n == 5 and m == 3):
#1 * 5 = 5
#2 * 5 = 10
#3 * 5 = 15

def multi(n, m):
    for i in range(1,m+1):
        print (i*n)

